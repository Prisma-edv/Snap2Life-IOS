#import "s2lsnaphistory.h"

@implementation s2lsnaphistory

@synthesize snapdate = _snapdate;
@synthesize name = _name;

- (id)initWithName:(NSString *)snapdate name:(NSString *)name;

{
    
    self = [super init];
    
    if (self) {
        self.snapdate = snapdate;
        self.name = name;
    }
    return self;
}

@end
